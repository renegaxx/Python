faturamento = 1000
custo = 700
lucro = faturamento - custo

print(f"Faturamento: {faturamento}, custo: {custo}, lucro: {lucro}") # O f serve para formatar o texto, ao introduzir chaves ({}), é possível colocar dentro das chaves a sua variavel dentro do texto.

print("Faturamento:" + str(faturamento) + ", custo :" + str(custo) + ", lucro :" + str(lucro))

print("Faturamento:" + "1000") # O + serve para juntar dois textos diferentes no mesmo print. não é possivel juntar numero com texto. 

print("Faturamento:" + str(faturamento)) # O str serve para introduzir a string

